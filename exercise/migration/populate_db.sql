-- Creating muscle_groups table
CREATE TABLE muscle_groups (
mg_id SERIAL PRIMARY KEY,
mg_name VARCHAR(40) NOT NULL,
created_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
update_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
deleted_at   TIMESTAMP DEFAULT NULL
);

-- Creating exercise table
CREATE TABLE exercise (
e_id SERIAL PRIMARY KEY,
e_name VARCHAR(40) NOT NULL,
created_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
update_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
deleted_at   TIMESTAMP DEFAULT NULL
);

-- Creating muscle_exercise_mapping table
CREATE TABLE muscle_exercise_mapping (
m_id SERIAL PRIMARY KEY,
e_id INTEGER NOT NULL,
mg_id INTEGER NOT NULL,
is_uses BOOLEAN,
FOREIGN KEY (e_id) REFERENCES exercise (e_id),
FOREIGN KEY (mg_id) REFERENCES muscle_groups (mg_id),
created_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
update_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
deleted_at   TIMESTAMP DEFAULT NULL
);

-- creating indices
CREATE INDEX idx_e_id ON muscle_exercise_mapping(e_id);
CREATE INDEX idx_mg_id ON muscle_exercise_mapping(mg_id);

